from datetime import datetime

# 当前日期
CURRENT_DATE = datetime.now().strftime("%Y年%m月%d日")


# ==================== 范围澄清阶段 ====================
CLARIFY_PROMPT = """你是一个专业的研究助理。用户提出了以下研究需求：

"{query}"

请分析这个需求，判断是否需要进一步澄清。以下情况需要澄清：
1. 需求范围过于宽泛（如"分析AI行业"）
2. 时间范围不明确（需要聚焦哪个时间段）
3. 地域范围不明确（需要聚焦哪个国家/地区）
4. 研究深度不明确（概述性了解还是深度分析）
5. 输出格式不明确（需要报告还是摘要）

如果需求足够明确，回复：
```json
{{"needs_clarification": false, "reason": "需求已明确，可直接开始研究"}}
```

如果需要澄清，回复最多3个关键问题：
```json
{{
  "needs_clarification": true,
  "questions": [
    "您希望聚焦哪个行业领域？（如：医疗、金融、制造等）",
    "研究的时间范围是什么？（如：近一年、近三年、历史发展）",
    "您需要哪种形式的输出？（如：详细报告、简要摘要、对比分析）"
  ]
}}
```

只返回JSON，不要添加其他内容。"""


# ==================== 规划阶段 ====================
PLAN_PROMPT = """你是一个专业的研究规划师。用户的研究需求如下：

原始需求："{query}"
{clarified_requirements}

请制定一份详细的研究计划，将研究任务拆解为可执行的子任务。

要求：
1. 每个子任务应聚焦一个具体主题
2. 每个子任务应包含3-5个搜索关键词
3. 设置优先级（1最高，5最低）
4. 总共不超过6个子任务

回复格式：
```json
{{
  "research_goal": "研究目标的一句话概括",
  "sub_tasks": [
    {{
      "topic": "子任务主题",
      "description": "简要描述需要收集的信息",
      "keywords": ["关键词1", "关键词2", "关键词3"],
      "priority": 1,
      "search_strategy": "广度扫描 | 深度挖掘 | 边缘探索"
    }}
  ],
  "expected_sections": ["报告章节1", "报告章节2", "报告章节3"]
}}
```

只返回JSON，不要添加其他内容。"""


# ==================== 评估阶段 ====================
EVALUATE_PROMPT = """你是一个信息质量评估专家。请评估以下搜索结果的质量：

当前子任务：{current_topic}
搜索查询：{search_query}
搜索结果：
{search_results}

请从以下维度评估信息质量（每项0-1分）：

1. **完整性**：信息是否覆盖了子任务的核心需求
2. **权威性**：信息来源的可靠性（官方、学术、权威媒体 > 普通媒体 > 博客/论坛）
3. **时效性**：信息的时间相关性（当前日期：{current_date}）
4. **多样性**：观点和来源的多样性

回复格式：
```json
{{
  "scores": {{
    "completeness": 0.7,
    "authority": 0.8,
    "timeliness": 0.6,
    "diversity": 0.5
  }},
  "overall_score": 0.65,
  "strengths": ["优点1", "优点2"],
  "weaknesses": ["不足1", "不足2"],
  "is_sufficient": false
}}
```

只返回JSON，不要添加其他内容。"""


# ==================== 综合阶段 ====================
SYNTHESIZE_PROMPT = """你是一个专业的研究报告撰写专家。基于以下收集的所有信息，撰写一份结构化的研究报告。

研究目标：{research_goal}
研究计划：{research_plan}

收集的所有信息：
{all_collected_info}

要求：
1. 按照研究计划的章节结构组织内容
2. 每个核心结论后用角标标注来源（如[1]、[2]）
3. 在文末提供完整的引用列表
4. 包含关键发现、趋势分析、建议等
5. 语言专业、逻辑清晰
6. 总字数控制在2000-3000字

输出格式：
---
# [报告标题]

## 研究摘要
[简要概括核心发现，3-5句话]

## 一、[章节标题]
[详细内容，每段结论标注来源[n]]

## 二、[章节标题]
[详细内容]

## 三、[章节标题]
[详细内容]

## 关键发现
1. [发现1]
2. [发现2]

## 建议
1. [建议1]
2. [建议2]

---
## 参考来源
[1] [标题](链接) - 发布时间
[2] [标题](链接) - 发布时间
...

直接输出报告内容，不要添加代码块标记或其他格式。"""